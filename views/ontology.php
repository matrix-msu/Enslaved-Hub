        <h2>Introduction</h2>
        <p>The Enslaved Ontology drives the data gathering and integration done as part of the project Enslaved: People of the Historic Slave Trade, funded by The Andrew W. Mellon Foundation through Michigan State University’s Matrix: The Center for digital Humanities & Social Sciences.</p>
        <p>The Enslaved Ontology is an OWL ontology for the historic slave trade. It focuses on historic persons (or agents), historic events, places and dates, records about events and historic persons, and provenance information. It also has accommodates contemporary persons (or agents), such as present-day researchers and research projects contributing to the data.</p>
